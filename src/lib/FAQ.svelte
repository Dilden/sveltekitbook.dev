<details class="open:bg-[#f97141] bg-[#333232] duration-300 my-5">
    <summary class="bg-inherit px-5 py-3 text-lg cursor-pointer">Why did you write this book?</summary>
    <div class="bg-slate-300 px-5 py-3 border border-[#1a1a1a] text-[#1a1a1a]">
      <p>This book was written to be concise and simple resource for other developers looking to learn a next generation JavaScript framework. It was written to provide the explanations and examples I wish I had when learning <a href='https://kit.svelte.dev' class='text-amber-600 hover:underline hover:text-amber-800'>SvelteKit</a>. The examples aim to be practical yet provide real-world demonstrations utilizing the concepts powering SvelteKit. After finishing this book, readers will have a high level understanding of SvelteKit and be able to build and release their very own SvelteKit-based applications and web sites.</p>
    </div>
</details>
<details class="open:bg-[#f97141] bg-[#333232] duration-300 my-5">
    <summary class="bg-inherit px-5 py-3 text-lg cursor-pointer">Who is this book for?</summary>
    <div class="bg-slate-300 px-5 py-3 border border-[#1a1a1a] text-[#1a1a1a]">
      <p>
        <b class='underline'>SvelteKit Up and Running</b> is intended for web developers looking to advance their skill set by learning
        the next popular JS-based framework. Whether you’re new to web development and deciding which framework should be the first
        you learn, or you want to set yourself apart from other job applicants by adding yet another popular
        technology to your résumé, this book will provide you with all of the essential knowledge to begin your
        journey to becoming a SvelteKit master!
    </p>
    </div>
</details>
<details class="open:bg-[#f97141] bg-[#333232] duration-300 my-5">
    <summary class="bg-inherit px-5 py-3 text-lg cursor-pointer">Do I need anything before starting?</summary>
    <div class="bg-slate-300 px-5 py-3 border border-[#1a1a1a] text-[#1a1a1a]">
      <p class='py-5'>
        Having a working knowledge of <a href='https://developer.mozilla.org/en-US/docs/Web/HTML'>HTML</a>,
        <a href='https://developer.mozilla.org/en-US/docs/Web/CSS'>CSS</a>, and 
        <a href='https://developer.mozilla.org/en-US/docs/Web/javascript'>JS</a> are a must before 
        beginning this book. Prior knowledge of <a href='https://svelte.dev'>Svelte</a> and how it works will be very beneficial.
      </p>
      <p class='py-5'>
        It also helps to have a computer to follow along with the provided code. Following along with the examples will ensure you're getting the experience inteded by the author. You'll need a MacOS, Windows, or Linux based computer with access to console or terminal for executing commands. You'll also need <a href='https://github.com/nvm-sh/nvm'>Node.js v16 or higher</a> installed on your computer as well as a text editor. The code found in this book has been made <a href='https://github.com/PacktPublishing/SvelteKit-Up-and-Running'>freely available on GitHub</a>. Likewise, the code for this very website is <a href='https://github.com/Dilden/sveltekitbook.dev'>also available on GitHub</a>.
      </p>
    </div>
</details>
<details class="open:bg-[#f97141] bg-[#333232] duration-300 my-5">
    <summary class="bg-inherit px-5 py-3 text-lg cursor-pointer">What's in this book?</summary>
    <div class="bg-slate-300 px-5 py-3 border border-[#1a1a1a] text-[#1a1a1a]">
      <p class='my-5'>
        In the first part, you’ll be introduced to SvelteKit with a quick installation, followed up with the standard
        Hello, World! example we’ve all come to love and appreciate. This will help familiarize you with SvelteKit's
        project structure. It then goes on to lay some groundwork by exploring the various configuration options available to further
        customize a SvelteKit application. From there, it covers how SvelteKit leverages existing standards
        to deliver a small yet powerful framework.
      </p>
      <p class='my-5'>
        In the second part, we learn why the developers of SvelteKit have made
        routing central to the framework. It examines some previously introduced routing techniques
        in more detail. This part then shows how SvelteKit moves data to Svelte components and accepts data from
        them through HTML form elements. Finally, you’ll see some more advanced routing techniques that
        promise to cover even the most obscure edge cases encountered in routing.
      </p>
      <p class='my-5'>
        Part three aims to cover the additional requisite concepts of SvelteKit. It begins by showing you how to
        produce a build for a production environment with resources showing how easily it can be done using
        SvelteKit’s adapter system. It then covers how hooks can be used to manipulate data flowing in and out
        of a SvelteKit application. From there, it addresses how to best import static assets by leveraging Vite.
        Then, it explains the various modules available in SvelteKit that make the entire framework possible.
        The section also covers how to ensure that a SvelteKit application can be made available to as many
        users as possible while boosting search engine rankings. Finally, it wraps up with various resources
        that will prove invaluable to any developer attempting to power their next project with SvelteKit.
      </p>
    </div>
</details>
