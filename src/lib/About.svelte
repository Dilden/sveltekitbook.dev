<h3>About the Book</h3>
<p>
This book aims to be an easy-to-read guide introducing you to the core concepts of the refreshingly simple JavaScript (JS) framework known
as SvelteKit. It was written to highlight what I felt were the core concepts of the framework. Because
the JS landscape changes so frequently, this text should not be considered an authority on SvelteKit.
Rather, the intention behind this book is to provide a resource that I wish was available to me when
I was learning this promising new framework.
</p>
